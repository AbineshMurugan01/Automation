package week1.day2;

public class Forloop {

	public static void main(String[] args) {

		int num =10;
				
				for (int i = 0; i<num; i++) {
					
					System.out.println("Increment :" +i);
					
				}
				
				for (int i=20; i>=num; i--) {
					
					System.out.println("Decrement :" +i);
				}
	}

}
